import mongoose from "mongoose";

//participant data

const participantSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
});

const Participant = mongoose.model("Participant", participantSchema);

export default Participant;
